const fs = require('fs');
const path = require('path');

console.log('Adding QR code endpoint for frontend integration...');

try {
  // Path to server.js
  const serverJsPath = '/home/applegraphicshyd/Onnrides-WhatsApp/src/server.js';
  let content = fs.readFileSync(serverJsPath, 'utf8');
  
  // Look for the position to insert the QR code endpoint
  const marker = "app.get('/api/status', validateApiKey, (req, res) => {";
  const position = content.indexOf(marker);
  
  if (position === -1) {
    throw new Error("Could not find the status endpoint to add QR code endpoint");
  }
  
  // QR code storage in memory
  const qrCodeStorageCode = `
// QR code storage in memory
let latestQrCode = null;

// Capture QR codes generated by WhatsApp
whatsappManager.on('qr', (data) => {
  console.log('QR code event received:', data.instanceId);
  latestQrCode = data.qr;
});

// Clear QR code on authentication
whatsappManager.on('authenticated', () => {
  latestQrCode = null;
});

`;

  // New QR code endpoint to add
  const qrEndpoint = `
// Endpoint to get QR code for frontend
app.get('/api/qr', validateApiKey, (req, res) => {
  if (latestQrCode) {
    res.json({
      success: true,
      qrCode: latestQrCode,
      message: 'QR code available'
    });
  } else {
    res.json({
      success: false,
      message: 'No QR code currently available. WhatsApp may already be authenticated or initializing.'
    });
  }
});

`;

  // Insert the QR code storage before the server starts listening
  const listenMarker = "// Start the server";
  const listenPosition = content.indexOf(listenMarker);
  
  if (listenPosition === -1) {
    throw new Error("Could not find the server start section");
  }
  
  // Insert the QR code storage logic before the server starts
  const modifiedContent = 
    content.substring(0, listenPosition) + 
    qrCodeStorageCode + 
    content.substring(listenPosition);
    
  // Insert the QR endpoint before the status endpoint
  const finalContent = 
    modifiedContent.substring(0, position) + 
    qrEndpoint +
    modifiedContent.substring(position);
    
  // Write the updated content back to the file
  fs.writeFileSync(serverJsPath, finalContent);
  console.log('QR code endpoint added successfully.');
  
} catch (err) {
  console.error('Error adding QR endpoint:', err);
} 